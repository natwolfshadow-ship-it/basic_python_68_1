"""
#
# Lists
#
"""
this_list = ["apple", "banana", "cherry"]
print(this_list)
print(this_list[1])  # Accessing first element
print(type(this_list)) # Checking type
print(len(this_list)) # Length of the list

"""
#
# Tuples
#
"""
this_tuple = ("apple", "banana", "cherry")
print(this_tuple)
print(this_tuple[1])  # Accessing first element
print(type(this_tuple)) # Checking type
print(len(this_tuple)) # Length of the tuple

"""
#
# Sets
#
"""
this_set = {"apple", "banana", "cherry"}
print(this_set)
print(type(this_set)) # Checking type
print(len(this_set)) # Length of the set

"""
#
# Dictionaries
#
"""
this_dict = {
    "brand": "Ford",
    "model": "Mustang",
    "year": 1964
}
print(this_dict)
print(this_dict["brand"])
print(type(this_dict)) # Checking type
print(len(this_dict)) # Length of the dictionary